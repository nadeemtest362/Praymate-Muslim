/**
 * Get a verse for the current time period
 * Based on day of year and whether it's morning (4 AM - 4 PM) or evening (4 PM - 4 AM)
 */
export const getDailyVerse = (): BibleVerse => {
  const now = new Date();
  const dayOfYear = Math.floor((now.getTime() - new Date(now.getFullYear(), 0, 0).getTime()) / (1000 * 60 * 60 * 24));
  const currentHour = now.getHours();
  
  // Morning: 4 AM - 4 PM, Evening: 4 PM - 4 AM
  const isMorning = currentHour >= 4 && currentHour < 16;
  
  // Create unique index for morning/evening + day combination
  const timeSlotIndex = (dayOfYear * 2) + (isMorning ? 0 : 1);
  const verseIndex = timeSlotIndex % DAILY_VERSES.length;
  
  return DAILY_VERSES[verseIndex];
};
